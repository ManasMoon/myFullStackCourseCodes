# Using an array (list) to store data
toys = ["Car", "Ball", "Doll"]
print(toys)

# Accessing elements using an index
print(toys[0])  # Output: Car
print(toys[1])  # Output: Ball
print(toys[2])  # Output: Doll

# Adding a new element
toys.append("Puzzle")
print(toys)  # Output: ['Car', 'Ball', 'Doll', 'Puzzle']