# if statement
age = 19
if age >= 18:
    print("You are an adult.")

# if else statement
age = 17
if age >= 18:
    print("You are an adult.")
else:
    print("You are not an adult.")

# if elif else statement
age = 10
if age >= 18:
    print("You are an adult.")
elif age >= 13:
    print("You are a teenager.")
else:
    print("You are a child.")
