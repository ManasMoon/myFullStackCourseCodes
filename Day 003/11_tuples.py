# Creating an empty tuple
empty_tuple = ()
print(empty_tuple)  # Output: ()
print(type(empty_tuple)) #Output: <class 'tuple'>

# Creating a tuple with one item (note the comma)
single_item_tuple = (1,)
print(single_item_tuple)  # Output: (1,)

# Creating a tuple with multiple items
multiple_items_tuple = (1, 7, 2)
print(multiple_items_tuple)  # Output: (1, 7, 2)

# Creating a tuple without parentheses (using commas only)
another_tuple = 1, 7, 2
print(another_tuple)  # Output: (1, 7, 2)

